class charsets {


    // ------------------------------------------------------------------------------

    public static void main( String[] args ) 
    {

        makeLine();

        showRegularASCII_chars();

        makeLine();


    } // end of main

    // ------------------------------------------------------------------------------

    private static void makeLine() 
    {
        System.out.println("\n******************************************\n");

    }


    // ------------------------------------------------------------------------------
    
    private static void showRegularASCII_chars() 
    {
        for ( int i=33; i<127; i++ )
        {
            System.out.print("[char(" + i + ") => " + (char)i + "] ");
        }
    } 




    // ------------------------------------------------------------------------------
    // ------------------------------------------------------------------------------





}  // end of class
